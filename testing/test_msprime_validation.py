"""
Validate the VCF-to-SFS parser against a ground-truth spectrum from msprime/tskit.

A tree sequence simulated under a two-epoch demography has an exact unfolded SFS (from tskit's
``allele_frequency_spectrum``); its genotypes are written to a VCF in which tskit encodes the
ancestral allele as REF, so the parser recovers the correct polarisation with
``skip_non_polarized=False``. The parsed spectrum must reproduce the tskit SFS bin for bin.

The fixtures (VCF + expected SFS) are committed and generated by
``testing/generate_msprime_fixtures.py``; this test needs only sfsutils + numpy, so it runs in CI
without msprime/tskit.
"""
import json
import os
from unittest.mock import patch

import numpy as np
import pytest

import sfsutils as sf
from sfsutils.settings import Settings

VCF = "resources/msprime/two_epoch.vcf"
SFS = "resources/msprime/two_epoch.sfs.txt"
REF = "resources/msprime/two_epoch.ref.fasta.gz"

# joint (two-population) fixtures
JOINT_VCF = "resources/msprime/two_epoch_joint.vcf"
JOINT_SFS = "resources/msprime/two_epoch_joint.jsfs.json"
JOINT_POPS = "resources/msprime/two_epoch_joint.pops.json"

pytestmark = pytest.mark.skipif(
    not all(os.path.exists(p) for p in (VCF, SFS, REF)),
    reason="msprime fixtures absent",
)

requires_joint = pytest.mark.skipif(
    not all(os.path.exists(p) for p in (JOINT_VCF, JOINT_SFS, JOINT_POPS)),
    reason="joint msprime fixtures absent",
)


def _load_joint_pops():
    """Load the population -> samples mapping and per-population sample sizes for the joint fixture."""
    with open(JOINT_POPS) as f:
        cfg = json.load(f)

    return cfg["pops"], cfg["n"]


def _parsed_and_expected():
    Settings.disable_pbar = True
    expected = np.loadtxt(SFS, dtype=int)
    n = len(expected) - 1
    # skip_non_polarized=False -> use REF (the tskit ancestral allele) as ancestral;
    # subsample_mode='random' takes all haplotypes exactly at full sample size.
    spectra = sf.Parser(n=n, vcf=VCF, skip_non_polarized=False, subsample_mode="random").parse()
    parsed = np.array(spectra["all"].to_list()).astype(int)
    return parsed, expected, n


def test_parser_reproduces_msprime_sfs():
    parsed, expected, n = _parsed_and_expected()
    # polymorphic bins must match the tskit ground truth exactly
    np.testing.assert_array_equal(parsed[1:n], expected[1:n])


def test_parser_recovers_all_segregating_sites():
    parsed, expected, n = _parsed_and_expected()
    assert parsed[1:n].sum() == expected[1:n].sum() > 0


def test_subsample_modes_agree():
    """Probabilistic and random subsampling produce close spectra (replaces the betula test)."""
    Settings.disable_pbar = True
    nn = 10
    prob = sf.Parser(vcf=VCF, n=nn, stratifications=[], subsample_mode="probabilistic",
                     skip_non_polarized=False).parse().all
    rand = sf.Parser(vcf=VCF, n=nn, stratifications=[], subsample_mode="random", seed=2,
                     skip_non_polarized=False).parse().all
    poly = rand.data[1:nn] > 0
    rel_diff = np.abs(prob.data[1:nn] - rand.data[1:nn])[poly] / rand.data[1:nn][poly]
    assert prob.data[1:nn].sum() > 0 and rand.data[1:nn].sum() > 0
    assert rel_diff.mean() < 0.25


def test_target_site_counter_sets_total_n_sites():
    """The TargetSiteCounter scales the spectrum to the requested number of target sites."""
    Settings.disable_pbar = True
    spectra = sf.Parser(
        vcf=VCF, n=20, stratifications=[], skip_non_polarized=False, subsample_mode="random",
        fasta=REF,
        target_site_counter=sf.TargetSiteCounter(n_samples=100_000, n_target_sites=50_000),
    ).parse()
    assert spectra.n_sites.sum() == pytest.approx(50_000)


def test_parser_loads_vcf_from_url_offline():
    """The URL branch is exercised without real network by mocking the download to a local file."""
    Settings.disable_pbar = True
    url = "https://example.org/data/two_epoch.vcf"
    expected = np.loadtxt(SFS, dtype=int)
    n = len(expected) - 1
    with patch("sfsutils.io_handlers.download_if_url",
               side_effect=lambda path, *a, **k: VCF if path == url else path):
        spectra = sf.Parser(vcf=url, n=n, stratifications=[], skip_non_polarized=False,
                            subsample_mode="random").parse()
    np.testing.assert_array_equal(np.array(spectra["all"].to_list()).astype(int)[1:n], expected[1:n])


@requires_joint
def test_parser_reproduces_msprime_joint_sfs():
    """The parsed joint (two-population) SFS reproduces the tskit ground truth bin for bin.

    Using the full per-population sample size with ``random`` subsampling makes the hypergeometric
    down-projection deterministic, so the match is exact (as for the single-population fixture).
    """
    Settings.disable_pbar = True
    pops, n = _load_joint_pops()
    truth = np.asarray(sf.JointSFS.from_file(JOINT_SFS)).astype(int)

    spectra = sf.Parser(vcf=JOINT_VCF, pops=pops, n=n, skip_non_polarized=False,
                        subsample_mode="random").parse()

    assert isinstance(spectra, sf.JointSpectra)
    parsed = np.asarray(spectra["all"]).astype(int)
    assert parsed.shape == truth.shape
    np.testing.assert_array_equal(parsed, truth)


@requires_joint
def test_joint_marginal_matches_single_population():
    """Marginalizing the joint SFS onto one population reproduces that population's single-population SFS."""
    Settings.disable_pbar = True
    pops, n = _load_joint_pops()

    joint = sf.Parser(vcf=JOINT_VCF, pops=pops, n=n, skip_non_polarized=False,
                      subsample_mode="random").parse()

    # marginalize onto population A (axis 0) by summing over the other population
    marginal_a = np.asarray(joint["all"].marginalize([0])).astype(int)

    # parse population A alone from the same VCF
    sfs_a = sf.Parser(vcf=JOINT_VCF, n=n["A"], include_samples=pops["A"],
                      skip_non_polarized=False, subsample_mode="random").parse()
    one_d_a = np.asarray(sfs_a["all"].to_list()).astype(int)

    np.testing.assert_array_equal(marginal_a, one_d_a)


@requires_joint
def test_joint_composes_with_stratifications_and_filtrations():
    """Stratifications and filtrations compose with joint parsing: summing the stratified joint spectra
    recovers the unstratified one, and every type keeps the joint shape."""
    Settings.disable_pbar = True
    pops, n = _load_joint_pops()

    base = sf.Parser(vcf=JOINT_VCF, pops=pops, n=n, skip_non_polarized=False,
                     subsample_mode="random", filtrations=[sf.SNPFiltration()]).parse()

    stratified = sf.Parser(vcf=JOINT_VCF, pops=pops, n=n, skip_non_polarized=False,
                           subsample_mode="random", filtrations=[sf.SNPFiltration()],
                           stratifications=[sf.RandomStratification(n_bins=3, seed=1)]).parse()

    assert set(stratified.types) == {"bin0", "bin1", "bin2"}
    assert stratified.shape == base["all"].shape
    np.testing.assert_array_equal(
        np.asarray(stratified.all).astype(int), np.asarray(base["all"]).astype(int)
    )


@requires_joint
def test_joint_subsample_modes_agree():
    """Down-projecting below the full sample size, probabilistic and random joint subsampling agree closely."""
    Settings.disable_pbar = True
    pops, _ = _load_joint_pops()
    nn = {"A": 6, "B": 4}

    prob = np.asarray(sf.Parser(vcf=JOINT_VCF, pops=pops, n=nn, skip_non_polarized=False,
                                subsample_mode="probabilistic").parse()["all"])
    rand = np.asarray(sf.Parser(vcf=JOINT_VCF, pops=pops, n=nn, skip_non_polarized=False,
                                subsample_mode="random", seed=3).parse()["all"])

    assert prob.shape == (7, 5)
    # each site contributes unit mass in both modes, so the totals are identical
    assert prob.sum() == pytest.approx(rand.sum())

    # the interior distributions are close
    mask = rand > 5
    rel_diff = np.abs(prob[mask] - rand[mask]) / rand[mask]
    assert rel_diff.mean() < 0.3
